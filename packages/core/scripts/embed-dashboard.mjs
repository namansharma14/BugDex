// Inline the single-file dashboard build into a committed TS module so the
// server is self-contained everywhere. Tolerant: if the dashboard hasn't been
// built, the existing asset is kept (core can still build on its own).
import { readFile, writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const distHtml = join(here, "../../../apps/dashboard/dist/index.html");
const out = join(here, "../src/dashboard/asset.generated.ts");

let html;
try {
  html = await readFile(distHtml, "utf8");
} catch {
  console.warn(
    "[embed-dashboard] apps/dashboard/dist/index.html not found; keeping existing asset.",
  );
  process.exit(0);
}

const banner = "/* Generated by scripts/embed-dashboard.mjs — do not edit by hand. */";
await writeFile(out, `${banner}\nexport const DASHBOARD_HTML = ${JSON.stringify(html)};\n`, "utf8");
console.log(`[embed-dashboard] embedded ${html.length} bytes into asset.generated.ts`);
